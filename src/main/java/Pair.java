public class Pair {

    private double xCoor;
    private double yCoor;

    public Pair(double xCoor, double yCoor) {
        this.xCoor = xCoor;
        this.yCoor = yCoor;
    }

    public double getX() {
        return this.xCoor;
    }

    public double getY() {
        return this.yCoor;
    }
}
